
for i in range(5):  #This loop creates the variable "i", with the starting value zero
    print(i)        #It then goes through the code within the loop, and then increments i by one, it does this until i reaches the value we gave it (in this case: 5)

#Important note: The stopping number is EXCLUSIVE, which means the program will stop before i reaches that number, in this program it only counts up to 4 because it stops short of 5

